func TestRemove_DryRun_DoesNotWrite(t *testing.T) {
	path := filepath.Join(t.TempDir(), "cfg.toml")
	initial := "key = 1\n\n# begin\nbody\n# end\n"
	if err := os.WriteFile(path, []byte(initial), 0o644); err != nil {
		t.Fatal(err)
	}
	removed, err := blocktext.Remove(path, begin, end, true)
	if err != nil {
		t.Fatal(err)
	}
	if !removed {
		t.Error("dry-run should still report removed = true (what would happen)")
	}
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatal(err)
	}
	if string(data) != initial {
		t.Error("dry-run must not modify the file")
	}
}

func TestWrite_ReplacesLegacyLocalizedBeginMarker(t *testing.T) {
	// Older versions wrote the begin marker with a different human-readable
	// tail after the em dash (e.g. in Italian). Recognition goes through the
	// stable prefix: the block must be replaced, not duplicated.
	path := filepath.Join(t.TempDir(), "cfg.toml")
	legacy := "# begin — vecchio testo localizzato\nold body\n# end\n"
	if err := os.WriteFile(path, []byte(legacy), 0o644); err != nil {
		t.Fatal(err)
	}
	newBegin := "# begin — new english tail"
	if err := blocktext.Write(path, newBegin, end, "new body"); err != nil {
		t.Fatal(err)
	}
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatal(err)
	}
	want := newBegin + "\nnew body\n# end\n"
	if string(data) != want {
		t.Errorf("got %q, want %q", data, want)
	}
	if strings.Count(string(data), "# begin") != 1 {
		t.Errorf("begin marker duplicated:\n%s", data)
	}
}
