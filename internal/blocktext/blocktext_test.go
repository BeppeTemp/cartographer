func TestRemove_StripsBlock_KeepsRest(t *testing.T) {
	path := filepath.Join(t.TempDir(), "cfg.toml")
	initial := "key = \"value\"\n\n# begin\nbody\n# end\n"
	if err := os.WriteFile(path, []byte(initial), 0o644); err != nil {
		t.Fatal(err)
	}
	removed, err := blocktext.Remove(path, begin, end, false)
	if err != nil {
		t.Fatal(err)
	}
	if !removed {
		t.Error("expected removed = true")
	}
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatal(err)
	}
	// The blank line that separated the key from the block was part of the
	// file, not generated by the block itself — Remove only strips the marker
	// span (markers + one trailing newline), so it's preserved verbatim.
	if string(data) != "key = \"value\"\n\n" {
		t.Errorf("got %q", data)
	}
}

func TestRemove_DeletesFileIfLeftEmpty(t *testing.T) {
	path := filepath.Join(t.TempDir(), "cfg.toml")
	initial := "# begin\nbody\n# end\n"
	if err := os.WriteFile(path, []byte(initial), 0o644); err != nil {
		t.Fatal(err)
	}
	if _, err := blocktext.Remove(path, begin, end, false); err != nil {
		t.Fatal(err)
	}
	if _, err := os.Stat(path); !os.IsNotExist(err) {
		t.Error("expected file to be removed once left empty")
	}
}

