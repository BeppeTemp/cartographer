package blocktext_test

import (
	"os"
	"path/filepath"
	"strings"
	"testing"

	"github.com/BeppeTemp/cartographer/internal/blocktext"
)

const (
	begin = "# begin"
	end   = "# end"
)

func TestWrite_CreatesFileIfAbsent(t *testing.T) {
	path := filepath.Join(t.TempDir(), "sub", "cfg.toml")
	if err := blocktext.Write(path, begin, end, "body\n"); err != nil {
		t.Fatal(err)
	}
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatal(err)
	}
	want := "# begin\nbody\n# end\n"
	if string(data) != want {
		t.Errorf("got %q, want %q", data, want)
	}
}

func TestWrite_ReplacesExistingBlock_PreservesRest(t *testing.T) {
	path := filepath.Join(t.TempDir(), "cfg.toml")
	initial := "# user comment\nkey = \"value\"\n\n# begin\nold body\n# end\n\n[other]\nx = 1\n"
	if err := os.WriteFile(path, []byte(initial), 0o644); err != nil {
		t.Fatal(err)
	}
	if err := blocktext.Write(path, begin, end, "new body"); err != nil {
		t.Fatal(err)
	}
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatal(err)
	}
	want := "# user comment\nkey = \"value\"\n\n# begin\nnew body\n# end\n\n[other]\nx = 1\n"
	if string(data) != want {
		t.Errorf("got %q, want %q", data, want)
	}
}

